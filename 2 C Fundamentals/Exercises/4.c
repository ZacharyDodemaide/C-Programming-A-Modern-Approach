#include <stdio.h>

int main(void)
{
	int height, length, width, volume, weight;
	float height_f, length_f, width_f, volume_f, weight_f;

	printf("unitialised int values: %d\n%d\n%d\n%d\n%d\n", height, length, width, volume, weight);
	printf("unitialised float values %f\n%f\n%f\n%f\n%f\n", height_f, length_f, width_f, volume_f, weight_f);

	return 0;
}